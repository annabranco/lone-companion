export const getSettlementName = (ancestry: Ancestries | '') => {
    const name = {
        original: '',
        english: '',
    };
    const capitalize = (word: string) => word.charAt(0).toUpperCase() + word.slice(1);

    if (ancestry) {
        if (ancestry === Ancestries.Japanese) {
            const firstPart = pickFrom(JAPANESE_PART);
            const secondPart = pickFrom(JAPANESE_PART);

            name.original = `${capitalize(firstPart.japanese)}${secondPart.japanese}`;
            name.english = `${capitalize(firstPart.english)} ${secondPart.english}`;
        } else if (ancestry === Ancestries.Russian || ancestry === Ancestries.Eastern) {
            const firstPart = pickFrom(RUSSIAN_PREFIX);
            const secondPart = pickFrom(RUSSIAN_SUFIX);

            name.original = `${firstPart.russian}${secondPart.russian}`;
            name.english = `${firstPart.english} ${secondPart.english}`;
        } else if (ancestry === Ancestries.Chinese) {
            const firstPart = pickFrom(CHINESE_PART);
            const secondPart = pickFrom(CHINESE_PART);

            name.original = `${capitalize(firstPart.chinese)}${secondPart.chinese}`;
            name.english = `${capitalize(firstPart.english)} ${secondPart.english}`;
        } else if (ancestry === Ancestries.Spanish || ancestry === Ancestries.Portuguese) {
            const firstPart = pickFrom(SPANISH_PREFIX);
            const secondPart = pickFrom(SPANISH_SUFIX);

            const { genderAndNumber } = firstPart;
            const { variations } = secondPart;

            if (variations) {
                secondPart.spanish = variations[genderAndNumber];
            }

            name.original = `${firstPart.spanish} ${secondPart.spanish}`;
            name.english = `${secondPart.english}${firstPart.english}`;
        } else if (ancestry === Ancestries.Norse) {
            const firstPart = pickFrom(NORSE_PREFIX);
            const secondPart = pickFrom(NORSE_SUFIX);

            name.original = `${firstPart.norse}${secondPart.norse}`;
            name.english = `${firstPart.english} ${secondPart.english}`;
        } else if (ancestry === Ancestries.Germanic) {
            const firstPart = pickFrom(GERMANIC_PREFIX);
            const secondPart = pickFrom(GERMANIC_SUFIX);

            name.original = `${firstPart.germanic}${secondPart.germanic}`;
            name.english = `${firstPart.english} ${secondPart.english}`;
        } else {
            const firstPart = pickFrom(ENGLISH_PREFIX);
            const secondPart = pickFrom(ENGLISH_SUFIX);

            name.original = `${firstPart}${secondPart}`;
        }
    } else if (chance(15)) {
        const firstPart = pickFrom(SPANISH_PREFIX);
        const secondPart = pickFrom(SPANISH_SUFIX);

        const { genderAndNumber } = firstPart;
        const { variations } = secondPart;

        if (variations) {
            secondPart.spanish = variations[genderAndNumber];
        }

        name.original = `${firstPart.spanish} ${secondPart.spanish}`;
        name.english = `${secondPart.english}${firstPart.english}`;
    } else if (chance(40)) {
        const firstPart = pickFrom(ENGLISH_PREFIX);
        const secondPart = pickFrom(ENGLISH_SUFIX);
       
        name.original = `${firstPart}${secondPart}`;
    } else {
        const firstPart = pickFrom(NORSE_PREFIX);
        const secondPart = pickFrom(NORSE_SUFIX);

        name.original = `${firstPart.norse}${secondPart.norse}`;
        name.english = `${firstPart.english} ${secondPart.english}`;
    }

    return name;
};

